
public class MyVector {
	
	private double changeX;

	public MyVector(double changeX) {
		super();
		this.changeX = changeX;
		
	}
	/**
	 * @return the changeX
	 */
	public double getChangeX() {
		return changeX;
	}
	/**
	 * @param changeX the changeX to set
	 */
	public void setChangeX(double changeX) {
		this.changeX = changeX;
	}

}
